function showAlert()
{
  const alertDiv = document.getElementById('custom-alert');
  alertDiv.style.visibility = 'visible';  // 显示弹窗
}

function closeAlert()
{
  const alertDiv = document.getElementById('custom-alert');
  alertDiv.style.visibility = 'hidden';   // 关闭弹窗
}

function changeMouse()
{
  const changeDiv=document.getElementById('nailong');
  changeDiv.style.cursor='pointer';
}

